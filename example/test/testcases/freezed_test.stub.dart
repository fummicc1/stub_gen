extension FreezedTargetStubBuilder on FreezedTarget {
  static FreezedTarget build(
          {String owner = "stub",
          String name = "stub",
          String content = "stub"}) =>
      FreezedTarget(
          owner: owner ?? "stub",
          name: name ?? "stub",
          content: content ?? "stub");

  static FreezedTarget buildWithFromJson(
          {Map<String, dynamic> json = const {}}) =>
      FreezedTarget.fromJson(json ?? {});
}
